function getCurrentLang() {
  return localStorage.getItem('pura_lang') || 'tr';
}

function applyLanguage(lang) {
  const t = translations[lang];
  if (!t) return;

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) {
      if (t[key].includes('<')) {
        el.innerHTML = t[key];
      } else {
        el.textContent = t[key];
      }
    }
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (t[key] !== undefined) el.placeholder = t[key];
  });

  const flagImg = document.getElementById('lang-flag');
  if (flagImg) {
    flagImg.src = lang === 'tr' ? 'gorseller/genel/tr_bayrak.png' : 'gorseller/genel/en_bayrak.png';
    flagImg.alt = lang === 'tr' ? 'TR' : 'EN';
  }

  document.documentElement.lang = lang === 'tr' ? 'tr' : 'en';
  localStorage.setItem('pura_lang', lang);
}

function toggleLanguage() {
  applyLanguage(getCurrentLang() === 'tr' ? 'en' : 'tr');
}

/* Auto-init */
document.addEventListener('DOMContentLoaded', function () {
  applyLanguage(getCurrentLang());
});
